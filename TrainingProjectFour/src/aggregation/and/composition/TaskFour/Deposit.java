package aggregation.and.composition.TaskFour;

public class Deposit {
    private int id;
    private int balance;

    Deposit(int id){
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getBalance() {
        return balance;
    }

    public void setBalance(int balance) {
        this.balance = balance;
    }
}
